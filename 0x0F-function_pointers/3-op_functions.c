#include "3-calc.h"

/**
 * op_add - Add two numbers a and b
 * @a: integer first number
 * @b: integer second number
 *
 * Return: integer
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Subtract b from a
 * @a: integer first number
 * @b: integer second number
 *
 * Return: integer
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Multply two numbers a and b
 * @a: integer first number
 * @b: integer second number
 *
 * Return: integer
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Divide a by b
 * @a: integer first number
 * @b: integer second number
 *
 * Return: integer
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Find remainder of a divided by b
 * @a: integer first number
 * @b: integer second number
 *
 * Return: integer
 */

int op_mod(int a, int b)
{
	return (a % b);
}
